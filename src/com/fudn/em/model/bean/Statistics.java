package com.fudn.em.model.bean;

import java.util.List;

public class Statistics {

	private float[] musicGenre;
	private int totalOnlineUser;
	private List<PostAndUser> postAndUserList;
	
	public float[] getMusicGenre() {
		return musicGenre;
	}
	
	public void setMusicGenre(float[] musicGenre) {
		this.musicGenre = musicGenre;
	}
	
	public int getTotalOnlineUser() {
		return totalOnlineUser;
	}
	
	public void setTotalOnlineUser(int totalOnlineUser) {
		this.totalOnlineUser = totalOnlineUser;
	}
	
	public List<PostAndUser> getPostAndUserList() {
		return postAndUserList;
	}
	
	public void setPostAndUserList(List<PostAndUser> postAndUserList) {
		this.postAndUserList = postAndUserList;
	}
	
}
